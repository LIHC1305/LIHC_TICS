let n = parseInt(prompt("Ingresa un número:"));

switch(n) {
    case 1:
        alert("Coche");
        break;
    case 2:
        alert("Moto");
        break;
    case 3:
        alert("Bicicleta");
        break;
    case 4:
        alert("Camión");
        break;
    default:
        alert("No recibí una opción válida");
        break;
}